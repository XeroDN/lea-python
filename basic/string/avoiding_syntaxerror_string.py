name="my name's niroj"
print(name)
#  The apostrophe appears inside a set of double quotes, so the Python 
#  interpreter has no trouble reading the string correctly but if we 
#  had used single quotes, it would have been a syntax error.
name='my name\'s niroj'
print(name)
#  The backslash (\) is used to escape the apostrophe, allowing it to be
#  included in the string without causing a syntax error.