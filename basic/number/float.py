print(2.3 + 3.5)
# The addition operator (+) is used to add two floating-point numbers.
print(2.3 * 3.5)
# The multiplication operator (*) is used to multiply two floating-point numbers.
print(2.3 - 3.5)
# The subtraction operator (-) is used to subtract one floating-point number from another.
print(2.3 / 3.5)
# The division operator (/) is used to divide one floating-point number by another.
print(2.3 // 3.5)
# The floor division operator (//) is used to divide one floating-point number by another and return the largest integer less than or equal to the result.
print(2.3 ** 3.5)
# The exponentiation operator (**) is used to raise one floating-point number to the power of another.
print(2.3 % 3.5)
# The modulus operator (%) is used to return the remainder of the division of one floating-point number by another.

print(2.3 + 3.5 * 5)
# The order of operations is followed, so multiplication is performed before addition.
print((2.3 + 3.5) * 5)
# Parentheses can be used to change the order of operations.
print(2.3 + 3.5 * 5 - 1)

